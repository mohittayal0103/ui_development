let product = ["Apple","Mango","Banana","Orange","Papaya"];

//---------------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------------

// Map()

//-------------------------------------------------------------------
console.log("\n------------ Array Using Map() Function ------------ \n");
//-------------------------------------------------------------------

product.map( pname => {                   //map func will read individual product from array and pass it to pname
  console.log(pname);
});   

//-------------------------------------------------------------------
console.log("\n------------ Array Using Map() Function With Index------------ \n");
//-------------------------------------------------------------------

product.map( function(value, index){
  console.log(index, value);
});


//-------------------------------------------------------------------
console.log("\n------------ Array Using Map() Function With Index Using Fat Arrow----------- \n");
//-------------------------------------------------------------------

product.map((value, index) => {
  console.log(index, value);
});


//-------------------------------------------------------------------
console.log("\n------------ Array Using Map() Function Using Fat Arrow inline----------- \n");
//-------------------------------------------------------------------

product.map(pname => console.log(pname));                  //Map func will run only and only on array


//--------------------------------------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------------------------------------


//filter()

//-------------------------------------------------------------------
console.log("\n------------ Array Using Filter() ----------- \n");
//-------------------------------------------------------------------

product.filter((pname, index) => {
  console.log(index, pname);
});


//-----------------------------------------------------------------------------------------------------------------------------
//-----------------------------------------------------------------------------------------------------------------------------



//forEach()

//-------------------------------------------------------------------
console.log("\n------------ Array Using forEach() ----------- \n");
//-------------------------------------------------------------------


product.forEach((pname, index) => {
  console.log(index, pname);
});